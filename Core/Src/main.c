void past_errors(int error){
	for(int i = ERROR_COUNT-1; i > 0; i--){
		errors[i] = errors[i - 1];
	}
	errors[0] = error;
}

int error_sum(int index){
	int total_sum = 0;
	for(int i = 0; i < index; i++){
		total_sum += errors[i];
	}
	return total_sum;
}

